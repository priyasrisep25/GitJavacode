package com.syntax.class01;

public class PracticeVariables {

	public static void main(String[] args) {

		// programming for Primitive data type
		// variable for Whole# data type
		byte Small = 122; // max 127
		short Medium = 32767;// max 32767
		int large = 214748; // Max 2147483647 most popular
		long Exlarge = 92233689; // max 9,223,372,036,854,775,807

		System.out.println("List of Whole# from Min to Max!");
		System.out.println(Small);
		System.out.println(Medium);
		System.out.println(large);
		System.out.println(Exlarge);

		// variable for decimal date type
		System.out.println("List of decimal# from Min to Max!");
		float Min = 2.3867f; // small decimal
		double Max = 1000.456; // big decimal value & most popular

		System.out.println(Min);
		System.out.println(Max);

		// variable for non-numeric for data type

		System.out.println("List of Single charcter from number  to Alphabet!");

		char Number = '1';
		char symbol = '$';
		char Alpha = 'A';

		System.out.println(Number);
		System.out.println(symbol);
		System.out.println(Alpha);
		
		//Boolean example
		boolean Icecream=true;
		boolean water=false;
		
		System.out.println("Boolean Example!");
		
		System.out.println(Icecream);
		System.out.println(water);
		
		
		
		
		
		
		
		

	}

}
